export function t(lang, key) {
  return I18N[lang]?.[key] ?? I18N.pt[key] ?? key;
}

export function applyI18n(lang) {
  const L = lang === 'en' ? 'en' : 'pt';
  document.documentElement.dataset.lang = L;
  document.documentElement.lang = L === 'en' ? 'en' : 'pt-PT';

  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    if (!key) return;
    const value = t(L, key);
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      // handled via placeholder attrs below
    } else if (el.tagName === 'OPTION') {
      el.textContent = value;
    } else {
      el.textContent = value;
    }
  });

  document.querySelectorAll('[data-i18n-html]').forEach((el) => {
    const key = el.getAttribute('data-i18n-html');
    if (!key) return;
    el.innerHTML = t(L, key);
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (key) el.setAttribute('placeholder', t(L, key));
  });

  document.querySelectorAll('[data-i18n-aria]').forEach((el) => {
    const key = el.getAttribute('data-i18n-aria');
    if (key) el.setAttribute('aria-label', t(L, key));
  });

  // Flag buttons state
  document.querySelectorAll('[data-set-lang]').forEach((btn) => {
    const active = btn.getAttribute('data-set-lang') === L;
    btn.classList.toggle('is-active', active);
    btn.setAttribute('aria-pressed', active ? 'true' : 'false');
  });

  // WhatsApp default message follows language
  if (window.SITE) {
    window.SITE.whatsappMessage = t(L, 'wa.default');
    window.SITE._lang = L;
  }

  window.dispatchEvent(new CustomEvent('lnunes:lang', { detail: { lang: L } }));
}
